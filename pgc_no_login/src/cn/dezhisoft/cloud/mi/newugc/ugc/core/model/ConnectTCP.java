package cn.dezhisoft.cloud.mi.newugc.ugc.core.model;

/**
 * 私有协议连接
 * 
 * @author Rosson Chen
 *
 */
public class ConnectTCP {

	/** UGC gateway host address for private protocol*/
	private String host;

	/** UGC gateway port */
	private int port ;
	
	protected ConnectTCP(){
		host 		= "" ;
		port		= 0 ;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
}
